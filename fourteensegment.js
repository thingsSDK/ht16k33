function fixBitmap(bitmap) {
    let newBitmap = bitmap
        .filter(value => value !== undefined)
        .map(num => num);

    for (var i = newBitmap.length; i < 4; i++) {
        newBitmap.unshift([0, 0]);
    }

    return newBitmap;
}

